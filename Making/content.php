            <p>Bad Faith is an Ithaca College Senior Thesis film shot  
                in just six weeks. The crew was comprised of students, and the cast of local actors. 
                All equipment used was borrowed from Ithaca College. No one was paid a dime. The $4,000 
                budget covered space rental, gas, and food, and was paid out of pocket by Writer/ Director Naomi Noir.</p>
            <p>A full time student and single mother, Naomi brought her five-year-old daughter to the set to be entertained 
                by her kind crew members and accommodating actors, later to be carried unconscious to the car after falling 
                asleep in a velvet-cushioned pew (and once behind the organ).    
            </p>
            <p>After considerable time the film is ready to show itself in the light of day as the fun off-beat little project that it is! 
                Hopefully it is as enjoyable to watch as it was to make.
                
            </p>

