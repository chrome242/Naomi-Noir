                <h4>Artist Statement</h4>
                <p>
                  As a writer I like to pair unlikely characters together. Past stories have 
                  included couples as diverse as a Satanic Witch with a born-again Christian, to an 
                  anorexic woman with a Turkish man who likes curves. I take my characters
                  and drop them into situations as far outside of their comfort zones as I can
                  imagine them. This juxtaposition of extreme opposites creates the conflict that makes life so 
                  interesting and allows for the unique and unexpected to emerge. 
                </p>
